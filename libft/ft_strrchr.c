#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *t;

	t = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			t = (char *)s;
		s++;
	}
	if (c == '\0')
		t = (char *)s;
	return (t);
}
